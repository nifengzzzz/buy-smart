function setLang(lang) {
  localStorage.setItem('buysmart_lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (I18N[lang] && I18N[lang][key]) {
      if (el.tagName === 'INPUT' && el.type === 'email') {
        el.placeholder = I18N[lang][key];
      } else {
        el.textContent = I18N[lang][key];
      }
    }
  });
  // Update toggle buttons
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    const nextLang = lang === 'zh' ? 'en' : 'zh';
    btn.textContent = I18N[lang]['lang.toggle'];
    btn.setAttribute('title', I18N[lang]['lang.label']);
    btn.setAttribute('onclick', `setLang('${nextLang}')`);
  });
}

function toggleLang() {
  const current = localStorage.getItem('buysmart_lang') || 'zh';
  setLang(current === 'zh' ? 'en' : 'zh');
}

// Init on load
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('buysmart_lang') || 'zh';
  setLang(saved);
});
